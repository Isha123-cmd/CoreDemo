
public class EvenOdd {
    public static void main(String[] args) {
    	System.out.println("Even\tOdd ");
    	int i=1;
    	
    	while(i<=10)
    	{
    		if(i%2 == 1)
    		{
    			System.out.print(i+ "\t");
    		}
    		else
    		{
    			System.out.println(i); 
    	}
    	 
            i++;
	
}
    }
}
